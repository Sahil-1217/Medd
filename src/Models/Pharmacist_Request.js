const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const pharmaReqSchema = new Schema({
  Username: {
    type: String,
    required: true,
  },
  Name: {
    type: String,
    required: true,
  },
  Email: {
    type: String,
    required: true
  },
  Password: {
    type: String,
    required: true,
  },
  DateOfBirth:{
    type: Date,
    required: true,
  },
    HourlyRate: {
        type: Number,
        required: true,
    },
    Affiliation: {
        type: String,
        required: true,
    },
    EducationalBackground: {
        type: String,
        required: true,
    }

}, { timestamps: true });

const pharma_req = mongoose.model('Pharmacist_Request', pharmaReqSchema);
module.exports = pharma_req;
